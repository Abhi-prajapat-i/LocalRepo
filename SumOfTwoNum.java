import java.util.*;
public class SumOfTwoNum {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter First number :");
        int num1 = sc.nextInt();
        System.out.print("Enter second number :");
        int num2 = sc.nextInt();

        System.out.println("Sum is "+(num1+num2));
    }
}
